// Health check (com verifica√ß√£o de cache e Supabase)
app.get('/health', async (req, res) => {
    try {
        // Testa conex√£o com Supabase
        const { data, error } = await supabase.from('cotacoes').select('count', { count: 'exact', head: true });
        
        // Testa conex√£o com Redis
        const redisHealth = await healthCheck();
        
        res.json({ 
            status: error ? 'unhealthy' : 'healthy',
            database: error ? 'disconnected' : 'connected',
            cache: redisHealth ? 'connected' : 'disconnected',
            supabase_url: supabaseUrl,
            timestamp: new Date().toISOString()
        });
        
        if (error) {
            console.error('‚ùå Erro no health check Supabase:', error);
        }
    } catch (error) {
        console.error('‚ùå Erro no health check:', error);
        res.json({ 
            status: 'unhealthy',
            database: 'error',
            cache: 'error',
            error: error.message,
            timestamp: new Date().toISOString()
        });
    }
});

