// GET - Listar todas as cota√ß√µes (COM CACHE)
app.get('/api/cotacoes', async (req, res) => {
    try {
        console.log('üìã Buscando todas as cota√ß√µes...');
        
        const cacheKey = 'cotacoes:all';
        
        // 1. Tentar buscar do cache
        const cachedData = await getCache(cacheKey);
        if (cachedData) {
            console.log('‚úÖ Retornando do cache');
            return res.json(cachedData);
        }
        
        // 2. Se n√£o tiver cache, buscar do Supabase
        const { data, error } = await supabase
            .from('cotacoes')
            .select('*')
            .order('timestamp', { ascending: false });

        if (error) {
            console.error('‚ùå Erro ao buscar cota√ß√µes do Supabase:', error);
            throw error;
        }

        console.log(`‚úÖ ${data?.length || 0} cota√ß√µes encontradas no Supabase`);

        // 3. Salvar no cache (expira em 5 minutos)
        await setCache(cacheKey, data, 300);

        res.json(data || []);
    } catch (error) {
        console.error('‚ùå Erro ao buscar cota√ß√µes:', error);
        res.status(500).json({ 
            error: 'Erro ao buscar cota√ß√µes',
            details: error.message 
        });
    }
});

// GET - Buscar cota√ß√£o espec√≠fica (COM CACHE)
app.get('/api/cotacoes/:id', async (req, res) => {
    try {
        console.log(`üîç Buscando cota√ß√£o ID: ${req.params.id}`);
        
        const cacheKey = `cotacoes:${req.params.id}`;
        
        // 1. Tentar buscar do cache
        const cachedData = await getCache(cacheKey);
        if (cachedData) {
            console.log('‚úÖ Retornando do cache');
            return res.json(cachedData);
        }
        
        // 2. Se n√£o tiver cache, buscar do Supabase
        const { data, error } = await supabase
            .from('cotacoes')
            .select('*')
            .eq('id', req.params.id)
            .single();

        if (error) {
            if (error.code === 'PGRST116') {
                console.log('‚ö†Ô∏è Cota√ß√£o n√£o encontrada');
                return res.status(404).json({ error: 'Cota√ß√£o n√£o encontrada' });
            }
            console.error('‚ùå Erro ao buscar cota√ß√£o:', error);
            throw error;
        }

        console.log('‚úÖ Cota√ß√£o encontrada');

        // 3. Salvar no cache
        await setCache(cacheKey, data, 300);

        res.json(data);
    } catch (error) {
        console.error('‚ùå Erro ao buscar cota√ß√£o:', error);
        res.status(500).json({ 
            error: 'Erro ao buscar cota√ß√£o',
            details: error.message 
        });
    }
});

// POST - Criar nova cota√ß√£o (LIMPA CACHE)
app.post('/api/cotacoes', async (req, res) => {
    try {
        console.log('üìù Criando nova cota√ß√£o...');
        console.log('Dados recebidos:', req.body);
        
        const novaCotacao = {
            ...req.body,
            id: Date.now().toString(),
            timestamp: new Date().toISOString(),
            negocioFechado: req.body.negocioFechado || false
        };

        console.log('Dados a serem inseridos:', novaCotacao);

        const { data, error } = await supabase
            .from('cotacoes')
            .insert([novaCotacao])
            .select()
            .single();

        if (error) {
            console.error('‚ùå Erro ao inserir no Supabase:', error);
            throw error;
        }

        console.log('‚úÖ Cota√ß√£o criada com sucesso:', data);

        // Limpar cache para for√ßar atualiza√ß√£o
        await clearCache('cotacoes:*');

        res.status(201).json(data);
    } catch (error) {
        console.error('‚ùå Erro ao criar cota√ß√£o:', error);
        res.status(500).json({ 
            error: 'Erro ao criar cota√ß√£o',
            details: error.message 
        });
    }
});

// PUT - Atualizar cota√ß√£o (LIMPA CACHE)
app.put('/api/cotacoes/:id', async (req, res) => {
    try {
        console.log(`‚úèÔ∏è Atualizando cota√ß√£o ID: ${req.params.id}`);
        console.log('Dados recebidos:', req.body);
        
        const { data, error } = await supabase
            .from('cotacoes')
            .update({
                ...req.body,
                updatedAt: new Date().toISOString()
            })
            .eq('id', req.params.id)
            .select()
            .single();

        if (error) {
            if (error.code === 'PGRST116') {
                console.log('‚ö†Ô∏è Cota√ß√£o n√£o encontrada para atualizar');
                return res.status(404).json({ error: 'Cota√ß√£o n√£o encontrada' });
            }
            console.error('‚ùå Erro ao atualizar:', error);
            throw error;
        }

        console.log('‚úÖ Cota√ß√£o atualizada com sucesso');

        // Limpar cache para for√ßar atualiza√ß√£o
        await clearCache('cotacoes:*');

        res.json(data);
    } catch (error) {
        console.error('‚ùå Erro ao atualizar cota√ß√£o:', error);
        res.status(500).json({ 
            error: 'Erro ao atualizar cota√ß√£o',
            details: error.message 
        });
    }
});

